package com.project.user;

public class UserVO {
	
	private String id;
	private String m_pw;
	private String m_name;
	


	public String getId() {
		return id;
	}


	public void setId(String id) {
		this.id = id;
	}

	


	public String getM_pw() {
		return m_pw;
	}


	public void setM_pw(String m_pw) {
		this.m_pw = m_pw;
	}


	public String getM_name() {
		return m_name;
	}


	public void setM_name(String m_name) {
		this.m_name = m_name;
	}



	
	
}
